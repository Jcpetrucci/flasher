#!/bin/bash
clear
stty -echo
case $1 in
wag)
	pattern=(\
	'####            ' \
	'    ####        ' \
	'        ####    ' \
	'            ####' \
	'        ####    ' \
	'    ####        ' \
	);;
right)
	pattern=(\
	'##              ' \
	'  ##            ' \
	'    ##          ' \
	'      ##        ' \
	'        ##      ' \
	'          ##    ' \
	'            ##  ' \
	'              ##' \
	'                ' \
	);;
left)
	pattern=(\
        '              ##' \
        '            ##  ' \
        '          ##    ' \
        '        ##      ' \
        '      ##        ' \
        '    ##          ' \
        '  ##            ' \
        '##              ' \
        '                ' \
	);;
split)
	pattern=(\
	'       ##       ' \
	'      ####      ' \
	'     ##  ##     ' \
	'    ##    ##    ' \
	'   ##      ##   ' \
	'  ##        ##  ' \
	' ##          ## ' \
	'##            ##' \
	);;
pulse)
	pattern=(\
	' ###   ##   ### ' \
	'#   ###  ###   #' \
	);;
alt*)
	pattern=(\
	'########        ' \
	'                ' \
	'########        ' \
	'        ########' \
	'                ' \
	'        ########' \
	);;
*)
	pattern=(\
	'################' \
	'################' \
	'################' \
	'################' \
	'                ' \
	);;
esac
i=-1
while :; do
	(( i++ ))
	(( i == ${#pattern[@]} )) && i=0
	printf '%s\r' "${pattern[i]}"
	sleep .1
done
